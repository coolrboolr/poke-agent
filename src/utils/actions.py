from enum import Enum

class Action(Enum):
    """Enumeration of valid emulator actions."""

    A = "A"
    B = "B"
    UP = "UP"
    DOWN = "DOWN"
    LEFT = "LEFT"
    RIGHT = "RIGHT"
    START = "START"
    MOVE_1 = "MOVE_1"
    MOVE_2 = "MOVE_2"
    MOVE_3 = "MOVE_3"
    MOVE_4 = "MOVE_4"
    USE_POTION = "USE_POTION"

